            <h4 class="tit">家庭医生职业的起源</h4>
            <p>
              家庭医生，即私人医生，是对服务对象实行全面的、连续的、有效的、及时的和个性化医疗保健服务和照顾的新型医生。在中国，由于家庭医生还未成熟，所以还存在很多以电话咨询为主的顾问式的家庭医生。
              <br>2015年1月14日在西城区人代会上，北京市西城区副区长梁昌新介绍，正在试点“契约式家庭医生”服务，确保24小时内预约对口三甲医院的专家号，3天内安排就诊。
