            <p>
              国内，我国的家庭医生队伍发展缓慢，主要在上海等经济发达城市，出现了家庭医生队伍，主要的服务对象是中高收入家庭。发展缓慢的原因有四个方面：第一是周边的社区建立医疗服务中心。第二是国家医疗保险患者及社会医疗保险的患者不能享受在家里治疗的报销政策。第三是如果病人过多，没有过多的医生参与进来，不能使家庭医生的队伍壮大起来。第四是医院没有在家庭医生这方面过多的宣传，许多人对此还不了解。
              <br>国外，家庭医生制度在国外已经发展的很成熟。如（持有医疗保险的一部分）美国人看病先找自己的家庭医生。如果需要，由家庭医生安排病人住院或联系专科医生继续为病人服务。一般情况下，保险公司只负责经过家庭医生同意的继续治疗费用。医生、保险公司、医院之间为了保护自己的利益互相制约。（另一种医疗保险不需要家庭医生的同意也可以看专科，但保险费用要高一些。）在中国，不论自费公费，病人看病任意选专科，选医生。病人很盲目，医生也希望有更多的挂号收入。
